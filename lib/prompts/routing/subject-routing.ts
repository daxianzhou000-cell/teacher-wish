      "讲义表达要围绕概念、条件、模型、方法和典型变式展开，强调题型归纳与步骤规范。",
      "所有例题、课堂练习、课后练习、小测收尾都必须严格围绕当前主题，不得混入别的单元或题型。",
      "如果当前主题是一元一次方程，则不得混入函数代值、函数图像、坐标系、y=、直线图像等非本主题核心内容。",
      "例题怎么选：例题示范必须作为独立模块稳定出现，优先选择最能体现本节方法的代表题，不得被难点、解析或练习内容吞并。",
      "例题至少给出题目情境、规范思路和关键步骤，避免只给一句题目标题或把例题拆散到别的模块里。",
      "课堂练习怎么出：优先基础题、短链路题、单步或两步标准题，强调当堂巩固和方法定型。",
