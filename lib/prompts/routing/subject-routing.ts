export type SupportedSubject =
  | "语文"
  | "数学"
  | "英语"
  | "物理"
  | "化学"
  | "通用";

export function resolveSupportedSubject(subject: string): SupportedSubject {
  const value = subject.trim();

  if (value === "语文") return "语文";
  if (value === "数学") return "数学";
  if (value === "英语") return "英语";
  if (value === "物理") return "物理";
  if (value === "化学") return "化学";

  return "通用";
}

